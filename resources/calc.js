/**
 * @param {number} a
 * @param {number} b
 * @return {number}
 */
export function add(a, b) {
  return a + b;
}

/**
 * @param {number} a
 * @param {number} b
 * @return {number}
 */
export function substract(a, b) {
  return a - b;
}

/**
 * @param {number} a
 * @param {number} b
 * @return {number}
 */
export function multiply(a, b) {
  return a * b;
}

/**
 * @param {number} x
 * @return {number}
 */
export function square(x) {
  return x * x;
}
